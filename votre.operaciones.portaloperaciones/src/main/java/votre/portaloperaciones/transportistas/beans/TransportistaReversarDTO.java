package votre.portaloperaciones.transportistas.beans;

public class TransportistaReversarDTO {

    private String company;
    private String user;
    private String jsonReversar;
    private String state;
    private String descState;

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public String getUser() {
        return user;
    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getJsonReversar() {
        return jsonReversar;
    }

    public void setJsonReversar(String jsonReversar) {
        this.jsonReversar = jsonReversar;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getDescState() {
        return descState;
    }

    public void setDescState(String descState) {
        this.descState = descState;
    }

}
